float Get_Voltage(void)
{
    float Volt;
    uint32_t AD_Value;

    // 启动ADC转换
    HAL_ADC_Start(&hadc1);

    // 等待转换完成
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);

    // 获取ADC值
    AD_Value = HAL_ADC_GetValue(&hadc1);

    // 计算电压，假设使用的是12位ADC，Vref = 3.3V，增益为11
    Volt = (float)AD_Value / 4095 * 3.3 * 11;

    return Volt;
}
